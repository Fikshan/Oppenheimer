package pojo;

public class MessagePojo {

	private String data;
	private String status;

	// Getter Methods
	public String getData() {
		return data;
	}

	public String getStatus() {
		return status;
	}

	// Setter Methods
	public void setData(String data) {
		this.data = data;
	}

	public void setStatus(String status) {
		this.status = status;
	}
}
